package cycling;
/**
 * SegmentType enum class that holds all of the types of segments
 *  @author Harry Findlay, Vihan Sharma
 */
public enum SegmentType {
    /**
     * Sprint segment type
     */
    SPRINT,

    /**
     * 4th Category climb
     */
    C4,

    /**
     * 3rd Category climb
     */
    C3,

    /**
     * 2nd Category climb
     */
    C2,

    /**
     * 1st Category climb
     */
    C1,

    /**
     * Hors Category climb, the hardest climb
     */
    HC;
}
