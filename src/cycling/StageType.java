package cycling;

public enum StageType {
    /**
     * Used for flat stages
     */
    FLAT,

    /**
     * Used for rolling terrain stages
     */
    MEDIUM_MOUNTAIN,

    /**
     * Used for high mountain finishes or stages with a lot of elevation
     */
    HIGH_MOUNTAIN,

    /**
     * Used for Time Trial stages
     */
    TT;
}